a = 6
b = 4

#comparador de igualdad
resultado = a == b
print(f"resultado == : {resultado}")

#comparador de diferencia
resultado = a != b
print(f"resultado != : {resultado}")

#comparador de mayor que
resultado = a > b
print(f"resultado >: {resultado}")

#comparador mayor o igual
resultado = a >= b
print(f"resultado >= : {resultado}")

#comparador menor que
resultado = a < b
print (f"resultado < : {resultado}")

#comparador menor o igual que
resultado = a <= b
print (f"resultado <= : {resultado}")


